sub Run {
    my ( $Self, %Param ) = @_;

    my $Payload = $Kernel::OM->Get('Kernel::System::Token')->ExtractToken(
        Token => $Self->{Authorization}->{Token}
    );

    # check result
    if ( !$Payload ) {
        return $Self->_Error(
            Code => 'Object.Invalid',
        );
    }

    return $Self->_Success(
        Session => $Payload,
    );
}

1;


