sub Run {
    my ( $Self, %Param ) = @_;

    # perform pattern search
    my @TranslationList = $Kernel::OM->Get('Kernel::System::Translation')->TranslationList(
        UserID => $Self->{Authorization}->{UserID}
    );

    # return result
    return $Self->_Success(
        Translation => \@TranslationList,
    );
}

1;
